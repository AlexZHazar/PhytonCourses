#Умножать два числа(a-первый множитель,b-второй множитель)
def multiplication(a,b):
    return a*b

#функция делит 2 числа (x и y)
def division(x, y):
    return x / y
    
# Складывает два числа(a-первое слагаемое, b-второе слагаемое)
def plus(a, b):
    plus=a+b
    return plus

# вычитание lde вычитание двух чисел(а- уменьшаемое b-вычетаемое )
def mainus(a,b):
    mainus=a-b
    return mainus

# Возведение в степень n(a-число, b-степень)
def stepn(a, b):
    stepn=a**b
    return stepn

