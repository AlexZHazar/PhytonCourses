class Calculator:
    @staticmethod
    def add(a, b):
        """
           Складывает два значения.

           params:
                  a-первое слагаемое
                  b-второе слагаемое

           return: результат сложения a, b
        """
        return a+b





    @staticmethod
    def add(a, b):
        """
           Деление двух значений.

           params:
                  a-первое слагаемое
                  b-второе слагаемое

           return: результат деления a, b
        """
        return a/b





    
    @staticmethod
    def add(a, b):
        """
           Деление двух значений по модулю.

           params:
                  a-первое слагаемое
                  b-второе слагаемое

           return: результат деления по модулю a, b
        """
        return a%b